ALL_TYPES = [
    "normal", "fire", "water", "electric", "grass", "ice", "fighting", "poison",
    "ground", "flying", "psychic", "bug", "rock", "ghost", "dragon", "dark",
    "steel", "fairy",
]


def effectiveness(attacking_type: str, defending_types: list[str]) -> float:
    """Multiplier for attacking_type hitting a Pokemon with the given defending_types."""
    multiplier = 1.0
    chart_row = TYPE_CHART.get(attacking_type, {})
    for def_type in defending_types:
        multiplier *= chart_row.get(def_type, 1.0)
    return multiplier


def full_matchup(defending_types: list[str]) -> dict[str, float]:
    """Effectiveness of every attacking type against a Pokemon with the given defending_types."""
    return {atk: effectiveness(atk, defending_types) for atk in ALL_TYPES}
